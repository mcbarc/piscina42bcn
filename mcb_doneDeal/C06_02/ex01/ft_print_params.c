#include <unistd.h>

void	ft_printstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
	write(1, "\n", 1);
}

int	main(int argc, char **argv)
{
	int	i;

	if (argc <= 1)
		return (1);
	i = 1;
	while (i < argc)
	{
		ft_printstr(argv[i]);
		i++;
	}
	return (0);
}
